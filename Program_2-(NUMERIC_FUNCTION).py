# use min and max function to print the min or max number in a list
print (max(6,59,77,3,6,7,43,77,33,999,24)) # To find the max value

print (min(6,59,77,3,6,7,43,77,33,999,24)) # To find the min value

# use abs function to find the absolute value of the number
print (abs(-5)) # # absolute value removes the negative sign and shows distance from zero

